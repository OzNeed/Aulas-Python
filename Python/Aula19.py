"""
Operadores de comparação (relacionais)
OP......Significado.........Exemplo (True)
>.......maior...............2 > 1
>=......maior ou igual......2 >= 2
<.......menor...............1 < 2
<=......menor ou igual......2 <= 2
==......igual...............'a' == 'a'
!=......diferente...........'a' != 'b'
"""

maior = 2>1
maior_ou_igual = 2>=2
menor = 1<2
menor_ou_igual = 2<=2
igual = 'a' == 'a'
diferente = 'a' != 'b'

print (f'Exemplo maior:{maior}')
print (f'Exemplo maior ou igual:{maior_ou_igual}')
print (f'Exemplo menor:{menor}')
print (f'Exemplo menor ou igual:{menor_ou_igual}')
print (f'Exemplo igual:{igual}')
print (f'Exemplo diferente:{diferente}')