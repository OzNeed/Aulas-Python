print(1234)#por ser dinâmico ele entende que e número e qeu não necessita de aspas

# Aspas simples
print('Bruno Alves')
# Aspas duplas
print("Bruno Alves")
# Escape
print("Bruno \"Alves\"")#seria para quando for leito o código, seria para pular e continuar com o código e deixar o que está após a barra invertida
print("Sem o Escape,"' Bruno "Alves"') #aspas duplas dentro de aspas simples
print("Sem o Escape, Bruno 'Alves'") #aspas simples dentro de aspas duplas
# r
print(r"Bruno \"Alves\"")#para exibir o comando de escape tem pode fazer dessa maneira

